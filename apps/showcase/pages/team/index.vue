            <h2>Why we chose to continue this library</h2>
            <p>We have used OpenVue daily for few years across many big and small projects. We think it was one of the best UI libraries in the Vue ecosystem, and we did not want to see it disappear behind a closed license. Continuing it felt like the natural thing to do, not a business opportunity.</p>

            <h2>Why we have not started triaging issues yet</h2>
            <p>We know it can look like we are ignoring the issue tracker. We are not. We have migrated real projects between UI libraries before, and we know how painful it is when documentation and tooling lag behind. So instead of picking off individual bugs right away, we are spending this early period on documentation and a migration tool, because that is what most daily users need first.</p>
